-- 13B. TRAINING DATASETS – Versioned snapshots for model training
------------------------------------------------------------------------

-- Mortality prediction training set (PyHealth-compatible structure)
CREATE OR REPLACE TABLE HEALTH_AI_READY_DB.TRAINING_DATASETS.MORTALITY_PREDICTION_V1 AS
SELECT
    F.PATIENT_ID,
    F.AGE,
    F.GENDER,
    F.TOTAL_DIAGNOSES,
    F.UNIQUE_ICD_CODES,
    F.HAS_MI,
    F.HAS_HEART_FAILURE,
    F.HAS_ATRIAL_FIB,
    F.TOTAL_MEDICATIONS,
    F.ON_BETA_BLOCKER,
    F.ON_ACE_INHIBITOR,
    F.ON_ANTICOAGULANT,
    F.ON_STATIN,
    F.DISTINCT_DRUG_CLASSES,
    F.TOTAL_ENCOUNTERS,
    F.INPATIENT_VISITS,
    F.AVG_LENGTH_OF_STAY,
    F.LATEST_HBA1C,
    F.LATEST_LDL,
    F.ABNORMAL_LAB_RATIO,
    CASE WHEN E.DISCHARGE_STATUS = 'EXPIRED' THEN 1 ELSE 0 END AS LABEL_MORTALITY,
    'V1'                  AS DATASET_VERSION,
    CURRENT_TIMESTAMP()   AS CREATED_AT
FROM HEALTH_AI_READY_DB.FEATURE_STORE.PATIENT_CLINICAL_FEATURES F
LEFT JOIN (
    SELECT PATIENT_ID, DISCHARGE_STATUS
    FROM HEALTH_TRANSFORM_DB.ENCOUNTERS.FACT_ENCOUNTER
    QUALIFY ROW_NUMBER() OVER (PARTITION BY PATIENT_ID ORDER BY ADMIT_DATE DESC) = 1
) E ON F.PATIENT_ID = E.PATIENT_ID;

-- Cardiac rehab adverse-event prediction training set
CREATE OR REPLACE TABLE HEALTH_AI_READY_DB.TRAINING_DATASETS.CARDIAC_ADVERSE_EVENT_V1 AS
SELECT
    C.*,
    CASE WHEN C.SAFETY_FLAG OR C.EXERCISE_TERMINATED_EARLY THEN 1 ELSE 0 END AS LABEL_ADVERSE_EVENT,
    'V1'                  AS DATASET_VERSION,
    CURRENT_TIMESTAMP()   AS CREATED_AT
FROM HEALTH_AI_READY_DB.FEATURE_STORE.CARDIAC_REHAB_FEATURES C;

