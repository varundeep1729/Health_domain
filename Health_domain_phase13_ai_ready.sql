-- Patient-level feature table for mortality / readmission prediction
CREATE OR REPLACE TABLE HEALTH_AI_READY_DB.FEATURE_STORE.PATIENT_CLINICAL_FEATURES AS
SELECT
    P.PATIENT_ID,
    P.AGE,
    P.GENDER,
    P.ETHNICITY,

    -- Diagnosis features
    COALESCE(DX.TOTAL_DIAGNOSES, 0)         AS TOTAL_DIAGNOSES,
    COALESCE(DX.UNIQUE_ICD_CODES, 0)        AS UNIQUE_ICD_CODES,
    COALESCE(DX.HAS_MI, FALSE)              AS HAS_MI,
    COALESCE(DX.HAS_HF, FALSE)             AS HAS_HEART_FAILURE,
    COALESCE(DX.HAS_AFIB, FALSE)           AS HAS_ATRIAL_FIB,

    -- Medication features
    COALESCE(MED.TOTAL_MEDICATIONS, 0)       AS TOTAL_MEDICATIONS,
    COALESCE(MED.ON_BETA_BLOCKER, FALSE)    AS ON_BETA_BLOCKER,
    COALESCE(MED.ON_ACE_INHIBITOR, FALSE)   AS ON_ACE_INHIBITOR,
    COALESCE(MED.ON_ANTICOAGULANT, FALSE)   AS ON_ANTICOAGULANT,
    COALESCE(MED.ON_STATIN, FALSE)          AS ON_STATIN,
    COALESCE(MED.DRUG_CLASS_COUNT, 0)       AS DISTINCT_DRUG_CLASSES,

    -- Encounter features
    COALESCE(ENC.TOTAL_ENCOUNTERS, 0)        AS TOTAL_ENCOUNTERS,
    COALESCE(ENC.INPATIENT_COUNT, 0)        AS INPATIENT_VISITS,
    COALESCE(ENC.AVG_LOS_DAYS, 0)          AS AVG_LENGTH_OF_STAY,
    COALESCE(ENC.MAX_LOS_DAYS, 0)          AS MAX_LENGTH_OF_STAY,

    -- Lab features (latest)
    LAB.LATEST_HBA1C,
    LAB.LATEST_LDL,
    LAB.LATEST_HDL,
    LAB.LATEST_TOTAL_CHOLESTEROL,
    LAB.ABNORMAL_LAB_RATIO,

    CURRENT_TIMESTAMP()                      AS FEATURE_COMPUTED_AT

FROM HEALTH_TRANSFORM_DB.PATIENTS.DIM_PATIENT P

LEFT JOIN (
    SELECT
        PATIENT_ID,
        COUNT(*)                                      AS TOTAL_DIAGNOSES,
        COUNT(DISTINCT ICD_CODE)                      AS UNIQUE_ICD_CODES,
        MAX(CASE WHEN CARDIAC_CATEGORY = 'ACUTE_MI' THEN TRUE ELSE FALSE END) AS HAS_MI,
        MAX(CASE WHEN CARDIAC_CATEGORY = 'HEART_FAILURE' THEN TRUE ELSE FALSE END) AS HAS_HF,
        MAX(CASE WHEN CARDIAC_CATEGORY = 'ATRIAL_FIBRILLATION' THEN TRUE ELSE FALSE END) AS HAS_AFIB
    FROM HEALTH_TRANSFORM_DB.DIAGNOSES.FACT_DIAGNOSIS
    GROUP BY PATIENT_ID
) DX ON P.PATIENT_ID = DX.PATIENT_ID

LEFT JOIN (
    SELECT
        PATIENT_ID,
        COUNT(*)                                                                  AS TOTAL_MEDICATIONS,
        MAX(CASE WHEN DRUG_CLASS = 'BETA_BLOCKER'  THEN TRUE ELSE FALSE END)    AS ON_BETA_BLOCKER,
        MAX(CASE WHEN DRUG_CLASS = 'ACE_INHIBITOR'  THEN TRUE ELSE FALSE END)   AS ON_ACE_INHIBITOR,
        MAX(CASE WHEN DRUG_CLASS = 'ANTICOAGULANT'  THEN TRUE ELSE FALSE END)   AS ON_ANTICOAGULANT,
        MAX(CASE WHEN DRUG_CLASS = 'STATIN'          THEN TRUE ELSE FALSE END)  AS ON_STATIN,
        COUNT(DISTINCT DRUG_CLASS)                                                AS DRUG_CLASS_COUNT
    FROM HEALTH_TRANSFORM_DB.MEDICATIONS.FACT_MEDICATION
    GROUP BY PATIENT_ID
) MED ON P.PATIENT_ID = MED.PATIENT_ID

LEFT JOIN (
    SELECT
        PATIENT_ID,
        COUNT(*)                                                    AS TOTAL_ENCOUNTERS,
        SUM(CASE WHEN ENCOUNTER_TYPE = 'INPATIENT' THEN 1 ELSE 0 END) AS INPATIENT_COUNT,
        AVG(LENGTH_OF_STAY_DAYS)                                    AS AVG_LOS_DAYS,
        MAX(LENGTH_OF_STAY_DAYS)                                    AS MAX_LOS_DAYS
    FROM HEALTH_TRANSFORM_DB.ENCOUNTERS.FACT_ENCOUNTER
    GROUP BY PATIENT_ID
) ENC ON P.PATIENT_ID = ENC.PATIENT_ID

LEFT JOIN (
    SELECT
        PATIENT_ID,
        MAX(CASE WHEN TEST_NAME ILIKE '%hba1c%'       THEN RESULT_NUMERIC END) AS LATEST_HBA1C,
        MAX(CASE WHEN TEST_NAME ILIKE '%ldl%'          THEN RESULT_NUMERIC END) AS LATEST_LDL,
        MAX(CASE WHEN TEST_NAME ILIKE '%hdl%'          THEN RESULT_NUMERIC END) AS LATEST_HDL,
        MAX(CASE WHEN TEST_NAME ILIKE '%cholesterol%'  THEN RESULT_NUMERIC END) AS LATEST_TOTAL_CHOLESTEROL,
        ROUND(SUM(CASE WHEN COMPUTED_FLAG IN ('HIGH','LOW') THEN 1 ELSE 0 END)::FLOAT
              / NULLIF(COUNT(*), 0), 3) AS ABNORMAL_LAB_RATIO
    FROM HEALTH_TRANSFORM_DB.LAB_VITALS.FACT_LAB_RESULT
    GROUP BY PATIENT_ID
) LAB ON P.PATIENT_ID = LAB.PATIENT_ID;

ALTER TABLE HEALTH_AI_READY_DB.FEATURE_STORE.PATIENT_CLINICAL_FEATURES
