-- Cardiac rehab feature table (for PyHealth-style models: RETAIN, Transformer)
CREATE OR REPLACE TABLE HEALTH_AI_READY_DB.FEATURE_STORE.CARDIAC_REHAB_FEATURES AS
SELECT
    S.PATIENT_ID,
    S.REFERRAL_ID,
    S.SESSION_ID,
    S.SESSION_NUMBER,
    R.COMPUTED_RISK                  AS RISK_CATEGORY,
    R.LVEF_PERCENT,
    R.GXT_PEAK_METS,

    -- Session hemodynamics (normalised)
    S.RESTING_HR,
    S.PEAK_HR,
    S.RECOVERY_HR,
    S.HR_RECOVERY_DELTA,
    S.ACHIEVED_HRR_PERCENT,
    S.RESTING_BP_SYSTOLIC,
    S.PEAK_BP_SYSTOLIC,
    S.RPE_PEAK,
    S.SPO2_MIN,
    S.DURATION_MINUTES,
    S.ECG_MONITOR_MINUTES,
    S.SAFETY_FLAG,
    S.EXERCISE_TERMINATED_EARLY,

    -- Rolling window features (last 3 sessions)
    AVG(S.PEAK_HR) OVER (PARTITION BY S.PATIENT_ID ORDER BY S.SESSION_NUMBER
                          ROWS BETWEEN 2 PRECEDING AND CURRENT ROW) AS ROLLING_3_AVG_PEAK_HR,
    AVG(S.RPE_PEAK) OVER (PARTITION BY S.PATIENT_ID ORDER BY S.SESSION_NUMBER
                           ROWS BETWEEN 2 PRECEDING AND CURRENT ROW) AS ROLLING_3_AVG_RPE,
    AVG(S.DURATION_MINUTES) OVER (PARTITION BY S.PATIENT_ID ORDER BY S.SESSION_NUMBER
                                   ROWS BETWEEN 2 PRECEDING AND CURRENT ROW) AS ROLLING_3_AVG_DURATION,

    -- Progression indicators
    S.PEAK_HR - LAG(S.PEAK_HR) OVER (PARTITION BY S.PATIENT_ID ORDER BY S.SESSION_NUMBER) AS PEAK_HR_DELTA,
    S.RPE_PEAK - LAG(S.RPE_PEAK) OVER (PARTITION BY S.PATIENT_ID ORDER BY S.SESSION_NUMBER) AS RPE_DELTA,
    S.DURATION_MINUTES - LAG(S.DURATION_MINUTES) OVER (PARTITION BY S.PATIENT_ID ORDER BY S.SESSION_NUMBER) AS DURATION_DELTA,

    CURRENT_TIMESTAMP() AS FEATURE_COMPUTED_AT

FROM HEALTH_TRANSFORM_DB.CARDIAC_REHAB.FACT_REHAB_SESSION S
JOIN HEALTH_TRANSFORM_DB.CARDIAC_REHAB.FACT_REHAB_REFERRAL R
  ON S.REFERRAL_ID = R.REFERRAL_ID;

